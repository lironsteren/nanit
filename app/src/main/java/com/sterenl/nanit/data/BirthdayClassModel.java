package com.sterenl.nanit.data;

public class BirthdayClassModel {
    private String mMainTitle;
    private int mAgeImageId;
    private String mSubTitle;
    private int mBackground;
    private int mPlaceholder;
    private int mCameraIcon;

    public String getMainTitle() {
        return mMainTitle;
    }

    public void setMainTitle(String mainTitle) {
        mMainTitle = mainTitle;
    }

    public int getAgeImage() {
        return mAgeImageId;
    }

    public void setAgeImage(int ageImage) {
        mAgeImageId = ageImage;
    }

    public String getSubTitle() {
        return mSubTitle;
    }

    public void setSubTitle(String subTitle) {
        mSubTitle = subTitle;
    }

    public int getAgeImageId() {
        return mAgeImageId;
    }

    public void setAgeImageId(int ageImageId) {
        mAgeImageId = ageImageId;
    }

    public int getBackground() {
        return mBackground;
    }

    public void setBackground(int background) {
        mBackground = background;
    }

    public int getPlaceholder() {
        return mPlaceholder;
    }

    public void setPlaceholder(int placeholder) {
        mPlaceholder = placeholder;
    }

    public int getCameraIcon() {
        return mCameraIcon;
    }

    public void setCameraIcon(int cameraIcon) {
        mCameraIcon = cameraIcon;
    }
}